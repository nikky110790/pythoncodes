i = 1

# printing the cube of number
print("The cube is started")
while i <= 10:
    print("The cube of {0:2} is {1:4}".format(i, i ** i))
    i = i + 1

print("The cube is completed")

# Printing Reciprocals of the number
i = 1
print("The reciprocal is started")
while i <= 10:
    print("The reciprocal of {0:2} is {1:6.2f}".format(i, 1/i))
    i = i + 1
    
print("The reciprocal is completed")